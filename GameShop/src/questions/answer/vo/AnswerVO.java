package questions.answer.vo;

public class AnswerVO {
	private String ansId;
	private String qesId;
	private String adminId;
	private String ansCont;
	private String ansDate;
	public String getAnsId() {
		return ansId;
	}
	public void setAnsId(String ansId) {
		this.ansId = ansId;
	}
	public String getQesId() {
		return qesId;
	}
	public void setQesId(String qesId) {
		this.qesId = qesId;
	}
	public String getAdminId() {
		return adminId;
	}
	public void setAdminId(String adminId) {
		this.adminId = adminId;
	}
	public String getAnsCont() {
		return ansCont;
	}
	public void setAnsCont(String ansCont) {
		this.ansCont = ansCont;
	}
	public String getAnsDate() {
		return ansDate;
	}
	public void setAnsDate(String ansDate) {
		this.ansDate = ansDate;
	}
	
	
}
