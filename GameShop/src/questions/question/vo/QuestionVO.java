package questions.question.vo;

public class QuestionVO {

	private String qesId;
	private String memId;
	private String qesTitle;
	private String qesCont;
	private String qesDate;
	private String memName;
	public String getQesId() {
		return qesId;
	}
	public void setQesId(String qesId) {
		this.qesId = qesId;
	}
	public String getMemId() {
		return memId;
	}
	public void setMemId(String memId) {
		this.memId = memId;
	}
	public String getQesTitle() {
		return qesTitle;
	}
	public void setQesTitle(String qesTitle) {
		this.qesTitle = qesTitle;
	}
	public String getQesCont() {
		return qesCont;
	}
	public void setQesCont(String qesCont) {
		this.qesCont = qesCont;
	}
	public String getQesDate() {
		return qesDate;
	}
	public void setQesDate(String qesDate) {
		this.qesDate = qesDate;
	}
	public String getMemName() {
		return memName;
	}
	public void setMemName(String memName) {
		this.memName = memName;
	}
	
	
}
